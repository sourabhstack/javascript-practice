//Arithmetic Operators
let a = 10;
let b = 3;

console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Remainder:", a % b);
console.log("Power:", a ** b);



//Assignment Operators
let x = 10;

x += 5;
console.log(x);

x -= 2;
console.log(x);

x *= 3;
console.log(x);

x /= 2;
console.log(x);

//Comparison Operators
let a = 10;
let b = "10";
console.log(a == b);
console.log(a === b);
console.log(a > 5);
console.log(a < 20);

//Logical Operators
let age = 18;
let hasLicense = true;

console.log(age >= 18 && hasLicense);

console.log(age < 18 || hasLicense);

console.log(!hasLicense);


//Increment & Decrement
let count = 5;

count++;
console.log(count);

count--;

console.log(count);


//String Operators
let firstName = "Sourabh";
let lastName = "Sharma";

console.log(firstName + " " + lastName);